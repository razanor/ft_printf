#include "libft.h"

int	ft_wstrlen(wchar_t *str)
{	
	int i;
	int len;

	len = 0;
	i = 0;
	while (str[i])
	{
		len = len + ft_count_bytes(str[i]);
		i++;
	}
	return (len);
}